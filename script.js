function obtenerElementosAleatorios(lista, cantidad) {
    const listaClon = [...lista];
    const elementosAleatorios = [];
    while (elementosAleatorios.length < cantidad && listaClon.length > 0) {
        const indiceAleatorio = Math.floor(Math.random() * listaClon.length);
        elementosAleatorios.push(listaClon[indiceAleatorio]);
        listaClon.splice(indiceAleatorio, 1);
    }
    return elementosAleatorios;
}

